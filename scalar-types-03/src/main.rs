fn main() {
    let x: i8 = -10;
    println!("{}", x);

    let y: u8 = 10;
    println!("{}", y);

    let decimal = 02_55;
    println!("{}", decimal);

    let hex = 0xff;
    println!("{}", hex);

    let octal = 0o377;
    println!("{}", octal);

    let binary = 0b1111_1111;
    println!("{}", binary);

    let byte = b'@';
    println!("{}", byte);

    let t = true;
    println!("{}", t);

    let f = false;
    println!("{}", f);

    let c = '@';
    println!("{}", c);
}
